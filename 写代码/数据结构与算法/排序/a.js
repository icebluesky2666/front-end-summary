function flattenLeaves(arr) {
    const result = [];

    function traverse(node) {
        // 如果没有子节点，说明是叶子节点
        if (!node.children || node.children.length === 0) {
            const { name, id, file_id, target_id } = node;
            result.push({ name, id, file_id, target_id });
        } else {
            // 遍历子节点
            for (const child of node.children) {
                traverse(child);
            }
        }
    }

    for (const item of arr) {
        traverse(item);
    }

    return result;
}

